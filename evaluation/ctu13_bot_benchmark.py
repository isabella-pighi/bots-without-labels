"""Fine-resolution real-data benchmark: CTU-13 scenario 1 (Neris botnet).

Companion to :mod:`evaluation.cicids_bot_benchmark`. The CICIDS2017 flow export
we benchmark against is minute-quantised *at source* (timestamps like
``6/7/2017 8:59``), so the dense-timing rules (``same_instant_burst`` /
``local_burst``) and any sub-minute beaconing-regularity signal have no real
data to work with there. This benchmark exists to separate the **data limit**
from the **method limit**: does detection recover when the timestamps actually
carry sub-second resolution?

CTU-13 scenario 1 is an Argus bidirectional NetFlow capture whose ``StartTime``
column has **microsecond** precision (e.g. ``2011/08/10 09:46:53.047277``). One
infected host (``147.32.84.165``) runs the Neris botnet (spam + C2 + click
fraud). We build a rare-attack mix (a contiguous, cadence-preserving slice of the
botnet flows plus a sampled benign background) and report recall/precision
*honestly, as a comparison to the minute-quantised CICIDS result* -- not as a
production accuracy claim.

Provenance / licence:
    CTU-13 dataset, Stratosphere Laboratory, CTU University. Licence: CC-BY
    (https://creativecommons.org/licenses/by/2.0/). Cite: S. Garcia, M. Grill,
    J. Stiborek, A. Zunino, "An empirical comparison of botnet detection
    methods", Computers & Security 45 (2014) 100-123.

Label policy (CTU-13 labels are directional; see the scenario README):
    POSITIVE = ``From-Botnet`` (flows generated by the infected host).
    NEGATIVE = ``From-Normal`` / ``Normal`` plus sampled ``Background`` variants.
    EXCLUDED = ``To-Botnet`` / ``To-Normal`` (traffic *towards* those hosts from
        unknown computers -- ambiguous ground truth, not malicious per se). In
        scenario 1 there are no ``To-Botnet`` rows; the exclusion is defensive.

The flow file is gitignored and large, so this is a local/manual benchmark;
:mod:`tests.test_ctu13_benchmark` skips when it is absent.

Download (369 MB, under our 400 MB ceiling), then run:
    curl -o data/capture20110810.binetflow \
        https://mcfp.felk.cvut.cz/publicDatasets/CTU-Malware-Capture-Botnet-42/detailed-bidirectional-flow-labels/capture20110810.binetflow
    uv run --extra eif python -m evaluation.ctu13_bot_benchmark

A second, independent family -- CTU-13 scenario 3 / Rbot (Botnet-44, 640 MB,
*above* the 400 MB ceiling so it is an opt-in local fetch) -- is wired through the
same wrapper to test whether the diverse-bot win generalises beyond Neris:
    curl -o data/capture20110812.binetflow \
        https://mcfp.felk.cvut.cz/publicDatasets/CTU-Malware-Capture-Botnet-44/detailed-bidirectional-flow-labels/capture20110812.binetflow
    uv run --extra eif python -m evaluation.ctu13_bot_benchmark --scenario sc3
"""

from __future__ import annotations

import argparse
import tempfile
from pathlib import Path

import numpy as np
import pandas as pd

from bots_without_labels.evaluate import evaluate_injection
from bots_without_labels.ingest import load
from bots_without_labels.pipeline import HEURISTIC_CUTOFF, detect

# CTU-13 scenarios share the Argus directional-label format, so a single
# build_mix/run handles any of them -- only the capture file and its provenance
# differ. sc1/Neris is the original tracked benchmark; sc3/Rbot is a second,
# independent family used to test whether the diverse-bot win generalises beyond
# Neris (the asymmetric_degree + actor-band story). Each value is the (name,
# binetflow path, CTU-Malware-Capture id for the fetch URL).
SCENARIOS = {
    "sc1": {
        "name": "scenario 1 (Neris)",
        "binetflow": Path("data/capture20110810.binetflow"),
        "capture": "CTU-Malware-Capture-Botnet-42",
    },
    "sc3": {
        "name": "scenario 3 (Rbot)",
        "binetflow": Path("data/capture20110812.binetflow"),
        "capture": "CTU-Malware-Capture-Botnet-44",
    },
}
# Backward-compatible default (sc1): kept as a module constant because
# tests/test_ctu13_benchmark.py and the runner import it by name.
DEFAULT_BINETFLOW = SCENARIOS["sc1"]["binetflow"]
# Argus bidirectional NetFlow header (comma-separated).
LABEL_COLUMN = "Label"
TIME_COLUMN = "StartTime"
POSITIVE_PREFIX = "From-Botnet"
NEGATIVE_PREFIXES = (
    "Background",
    "To-Background",
    "From-Background",
    "From-Normal",
    "Normal",
)
# A contiguous head-of-capture slice of the botnet flows keeps the beacon cadence
# intact (random sampling would destroy the inter-arrival structure the timing
# rules read). Sized, with the benign sample, for a ~3% rare-attack base rate
# comparable to the CICIDS benchmark -- the whole scenario carries ~41k botnet
# flows from one host, far too many to leave a realistic minority without an
# intractably large benign pool.
N_BOT = 2_000
N_BENIGN = 60_000
SEED = 7


def _normalised_labels(frame: pd.DataFrame) -> pd.Series:
    """Strip the ``flow=`` prefix Argus/ralabel writes on the Label column."""

    return frame[LABEL_COLUMN].astype(str).str.replace("^flow=", "", regex=True)


def build_mix(
    path: Path = DEFAULT_BINETFLOW,
    *,
    n_bot: int = N_BOT,
    n_benign: int = N_BENIGN,
    seed: int = SEED,
):
    """Return a (frame-without-label, truth-array) rare-attack mix.

    A contiguous time-ordered slice of ``From-Botnet`` flows (cadence preserved)
    is mixed with a sampled benign background; ambiguous ``To-*`` labels are
    excluded entirely.
    """

    df = pd.read_csv(path, low_memory=False)
    df.columns = [c.strip() for c in df.columns]
    labels = _normalised_labels(df)

    df = df.assign(_label=labels, _ts=pd.to_datetime(df[TIME_COLUMN], errors="coerce"))
    bot = df[df["_label"].str.startswith(POSITIVE_PREFIX)].sort_values("_ts")
    benign_pool = df[df["_label"].str.startswith(NEGATIVE_PREFIXES)]

    bot_slice = bot.head(n_bot)
    take = min(n_benign, len(benign_pool))
    benign = benign_pool.sample(n=take, random_state=seed)

    mix = pd.concat([benign, bot_slice]).sort_values("_ts").reset_index(drop=True)
    truth = mix["_label"].str.startswith(POSITIVE_PREFIX).to_numpy().astype(int)
    frame = mix.drop(columns=[LABEL_COLUMN, "_label", "_ts"])
    return frame, truth


def run(
    path: Path = DEFAULT_BINETFLOW,
    *,
    n_bot: int = N_BOT,
    n_benign: int = N_BENIGN,
    seed: int = SEED,
):
    """Build the mix, run detection through the real loader, return metrics."""

    frame, truth = build_mix(path, n_bot=n_bot, n_benign=n_benign, seed=seed)
    with tempfile.TemporaryDirectory() as tmp:
        csv_path = Path(tmp) / "ctu13_bot_mix.csv"
        frame.to_csv(csv_path, index=False)
        loaded = load(csv_path)
    result = detect(loaded.frame, loaded.schema)

    report = evaluate_injection(result.is_bot, truth)
    report["flag_rate"] = float(np.mean(result.is_bot))
    report["base_rate"] = float(np.mean(truth))
    report["n_rows"] = int(len(truth))
    report["heuristic_flag_rate"] = float(np.mean(result.heuristic >= HEURISTIC_CUTOFF))
    report["ml_flag_rate"] = float(np.mean(result.ml_scores > result.ml_threshold))
    # The data property this benchmark turns on: the detected timestamp grid (the
    # mode positive gap between distinct timestamps, in seconds) and whether the
    # dense-timing rules were gated off as a coarse-grid artifact. CTU-13 is
    # sub-second, so the gate stays OPEN -- the opposite of CICIDS.
    thresholds = result.rules_result.thresholds
    report["timestamp_grid_seconds"] = thresholds.get("timestamp_grid")
    report["dense_timing_gated"] = thresholds.get("dense_timing_gated")
    return report


def _scenario_label(scenario_key: str, binetflow: Path | None) -> str:
    """Heading label for the effective capture.

    With no explicit ``--binetflow`` the chosen ``--scenario`` names itself. When
    ``--binetflow`` is given it is labelled by the matching registry scenario if
    the path is a known capture, otherwise by an explicit *custom capture*
    heading -- never by silently inheriting the ``--scenario`` default, which
    would mislabel (e.g. report the sc3/Rbot file under the sc1/Neris heading).
    """

    if binetflow is None:
        return SCENARIOS[scenario_key]["name"]
    for scenario in SCENARIOS.values():
        if Path(scenario["binetflow"]) == Path(binetflow):
            return scenario["name"]
    return f"custom capture ({binetflow})"


def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser(prog="ctu13_bot_benchmark")
    parser.add_argument(
        "--scenario",
        choices=sorted(SCENARIOS),
        default="sc1",
        help="CTU-13 scenario to run (default sc1/Neris; sc3 is Rbot)",
    )
    parser.add_argument(
        "--binetflow",
        type=Path,
        default=None,
        help="explicit binetflow path (overrides --scenario)",
    )
    parser.add_argument("--bot", type=int, default=N_BOT)
    parser.add_argument("--benign", type=int, default=N_BENIGN)
    args = parser.parse_args(argv)

    if args.binetflow is not None:
        path = args.binetflow
    else:
        path = SCENARIOS[args.scenario]["binetflow"]
    label = _scenario_label(args.scenario, args.binetflow)
    report = run(path, n_bot=args.bot, n_benign=args.benign)
    grid = report["timestamp_grid_seconds"]
    print(f"CTU-13 {label} fine-resolution benchmark")
    print(f"  rows                {report['n_rows']:>10d}")
    print(f"  base rate           {report['base_rate']:>10.4f}")
    print(
        f"  timestamp grid (s)  {grid:>10.6f}"
        if grid is not None
        else "  timestamp grid (s)        none"
    )
    print(f"  dense-timing gated  {str(report['dense_timing_gated']):>10}")
    print(f"  flag rate           {report['flag_rate']:>10.4f}")
    print(f"    heuristic         {report['heuristic_flag_rate']:>10.4f}")
    print(f"    ml                {report['ml_flag_rate']:>10.4f}")
    print(f"  recall              {report['recall']:>10.4f}")
    print(f"  precision           {report['planted_precision']:>10.4f}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
